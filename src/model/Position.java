package model;

/**
 *
 * @author rakib
 */
public class Position {
    private int x;
    private int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Position fix(Direction direction) {
        int newX = x + direction.getX();
        int newY = y + direction.getY();
        return new Position(newX, newY);
    }
}

